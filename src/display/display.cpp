#define FONT_W       8
#define FONT_H       8
#define FONT_SCALE   4
#define TEXT_W       (FONT_W * FONT_SCALE)
#define TEXT_H       (FONT_H * FONT_SCALE)
#define TEXT_PADDING 12
#define TEXT_LEN     11
#define BOX_W        (TEXT_LEN * TEXT_W + 2 * TEXT_PADDING)
#define BOX_H        (TEXT_H + 2 * TEXT_PADDING)
#define FG_COLOR     0x0000u
#define BOX_COLOR    0x0000u

/* Static row buffer to avoid stack overflow */
static uint16_t fill_row[480];
static uint16_t text_box[BOX_W * BOX_H];

static void text_box_clear(uint16_t color)
{
    for (size_t i = 0; i < ARRAY_SIZE(text_box); i++) {
        text_box[i] = color;
    }
}

static void text_box_draw_border(uint16_t color)
{
    for (uint16_t x = 0; x < BOX_W; x++) {
        text_box[x] = color;
        text_box[(BOX_H - 1) * BOX_W + x] = color;
    }

    for (uint16_t y = 0; y < BOX_H; y++) {
        text_box[y * BOX_W] = color;
        text_box[y * BOX_W + (BOX_W - 1)] = color;
    }
}

static void text_box_draw_char(char c, uint16_t x, uint16_t y)
{
    if (c < 0x20 || c > 0x7F) {
        c = '?';
    }

    const uint8_t *glyph = font8x8[(uint8_t)c - 0x20];

    for (uint16_t row = 0; row < FONT_H; row++) {
        for (uint16_t col = 0; col < FONT_W; col++) {
            if ((glyph[row] & (0x80u >> col)) == 0U) {
                continue;
            }

            for (uint16_t y_scale = 0; y_scale < FONT_SCALE; y_scale++) {
                const uint16_t out_y = y + row * FONT_SCALE + y_scale;

                for (uint16_t x_scale = 0; x_scale < FONT_SCALE; x_scale++) {
                    const uint16_t out_x = x + col * FONT_SCALE + x_scale;
                    text_box[out_y * BOX_W + out_x] = FG_COLOR;
                }
            }
        }
    }
}

static void text_box_draw_text(const char *str, uint16_t x, uint16_t y)
{
    while (*str != '\0') {
        text_box_draw_char(*str++, x, y);
        x += TEXT_W;
    }
}

bool app_display_touch_ready(void)
{
    return device_is_ready(touch);
}

void app_display_draw_text_box(const struct device *dev, uint16_t x, uint16_t y)
{
    struct display_buffer_descriptor desc = {
        .buf_size = sizeof(text_box),
        .width = BOX_W,
        .height = BOX_H,
        .pitch = BOX_W,
    };

    text_box_clear(APP_DISPLAY_BG_COLOR);
    text_box_draw_border(BOX_COLOR);
    text_box_draw_text("Hello World", TEXT_PADDING, TEXT_PADDING);
    display_write(dev, x, y, &desc, text_box);
}

void app_display_fill_screen(const struct device *dev, uint16_t color)
{
    struct display_capabilities caps;
    display_get_capabilities(dev, &caps);

    uint16_t w = MIN(caps.x_resolution, (uint16_t)ARRAY_SIZE(fill_row));
    for (uint16_t i = 0; i < w; i++) {
        fill_row[i] = color;
    }

    struct display_buffer_descriptor desc = {
        .buf_size = w * sizeof(uint16_t),
        .width = w,
        .height = 1,
        .pitch = w,
    };

    for (uint16_t y = 0; y < caps.y_resolution; y++) {
        display_write(dev, 0, y, &desc, fill_row);
    }
}
